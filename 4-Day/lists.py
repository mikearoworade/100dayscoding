# Data structure: A way of organizing and storing data in python
# Lists - stores pieces of related data orderly
states_of_nigeria = ["Lagos", "Abeokuta", "Benin", "Oyo"]

# Accessing lists data
print(states_of_nigeria)
print(states_of_nigeria[0])
print(states_of_nigeria[2])

# Alter lists data
states_of_nigeria[1] = "Ogun"
print(states_of_nigeria)

# Appending (adding) data
states_of_nigeria.append("Gombe")
print(states_of_nigeria)
